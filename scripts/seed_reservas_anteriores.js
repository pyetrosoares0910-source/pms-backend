function parseBrDateToISO(dateStr) {
  const [d, m, y] = dateStr.split("/").map(Number);
  return `${y}-${String(m).padStart(2, "0")}-${String(d).padStart(2, "0")}T00:00:00.000Z`;
}

async function findOrCreateGuest(name) {
  let guest = await prisma.guest.findFirst({
    where: { name: { equals: name, mode: "insensitive" } }
  });
  if (!guest) {
    guest = await prisma.guest.create({ data: { id: uuid(), name } });
  }
  return guest;
}

async function reservationExists(roomId, checkin, checkout) {
  return await prisma.reservation.findFirst({
    where: { roomId, checkinDate: new Date(checkin), checkoutDate: new Date(checkout) }
  });
}

